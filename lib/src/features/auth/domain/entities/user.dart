class User {
  final int? id;
  final String? firstname;
  final String? lastname;
  final String? name;
  final String? email;
  final int? emailVerify;
  final int? phoneVerify;
  final String? phone;
  final String? password;
  final String? address;
  final String? avatar;
  final int? status;

  const User({
    this.id,
    this.firstname,
    this.lastname,
    this.name,
    this.email,
    this.emailVerify,
    this.phone,
    this.password,
    this.address,
    this.avatar,
    this.status,
    this.phoneVerify,
  });
}
